    private void guessTxtKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_guessTxtKeyPressed
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        if(c == '#'){
            cheatLabel.setText("Hint: "+gameLogic.currentWord);
        }
        if (Character.isLetter(c) || Character.isISOControl(c)) {
            guessTxt.setEditable(true);
            cheatLabel.setText(null);
        } else {
            guessTxt.setEditable(false);
        }

    }//GEN-LAST:event_guessTxtKeyPressed

