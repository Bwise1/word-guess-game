    private void updateButtons(String str) {
        wordPanel.removeAll();
        wordPanel.setLayout(new GridLayout(1, 20));
        for (int i = 1; i <= str.length(); i++) {

            Buttons[i] = new JButton();
            Buttons[i].setVisible(true);
            Buttons[i].setPreferredSize(new Dimension(45, 45));

            Buttons[i].setText(gameLogic.maskedWord.charAt(i - 1) + "");
            wordPanel.add(Buttons[i]);
            wordPanel.revalidate();
            wordPanel.repaint();
            //wordPanel.add(Box.createHorizontalStrut(20));

        }
    }

