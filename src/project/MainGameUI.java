    private void revealBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_revealBtnActionPerformed
        // TODO add your handling code here:
        int noOfTrys = 0;
        
        try {
            guessed = guessTxt.getText().charAt(0);
            guessTxt.requestFocus();
            //System.out.println(guessed);
            char[] ch = gameLogic.currentWord.toCharArray();
            char[] ch2 = gameLogic.maskedWord.toCharArray();
            guessTxt.setText("");
            guessTxt.requestFocus();
            for (int i = 0; i < ch.length; i++) {
                if (ch[i] == guessed) {
                    ch2[i] = guessed;
                    guessedTxt.setText(""+guessed + " is found successfully");
                    gameLogic.maskedWord = new String(ch2);
                    updateButtons(gameLogic.maskedWord);
                } else  {
                    noOfTrys = noOfTrys + 1;
                }
            }//for loop
            if (noOfTrys == ch2.length) {
                guessedTxt.setText(""+guessed + " is a wrong guess");
                totalAttempts--;
                //System.out.println(totalAttempts);
                if(totalAttempts == 0){
                    JOptionPane.showMessageDialog(this, "You Ran out of Attempts try again!!!");
                    initialize();
                }
                //System.out.println(gameLogic.currentWord);
                attemptsTxt.setText("You have " + totalAttempts + " attempts left");
            }
            if(gameLogic.maskedWord.equals(gameLogic.currentWord)){
                    JOptionPane.showMessageDialog(this, "Correct Guess!!!");
                    wins = wins+1;
                    
                    if(noOfWords == gameMode){
                        if((gameMode == 4)&&(wins>=2)){
                            JOptionPane.showMessageDialog(this, "You Won this Section");
                        }
                        else if((gameMode == 6)&&(wins>=3)){
                            JOptionPane.showMessageDialog(this, "You Won this Section");
                        }
                        else{
                            JOptionPane.showMessageDialog(this, "You Lost this Section");
                        }
                    }
                    initialize();
                }
            
            //System.out.println(gameLogic.maskedWord);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Please Enter a letter!!");
            guessTxt.requestFocus();
        }
    }//GEN-LAST:event_revealBtnActionPerformed

